--原料对应的配方，用于打开合成界面
_G.MaterialIndex = {};

local function MakeMaterialIndexTable()
	for id,info in pairs(ProduceMaterial)do
		if info.material then
			for _,tb in pairs(info.material)do
				if tb.item then
					MaterialIndex[tb.item] = id;
				end
			end
		end
	end
end;

MakeMaterialIndexTable();

