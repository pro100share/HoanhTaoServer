_G.GetSwordFoundryConsumeInfo = function(num) -----num 为第几颗小星
	local consumeInfo = {dwMetalValue = 0,dwWoodValue = 0,dwWaterValue = 0,dwFireValue = 0,dwEarthValue = 0,dwSwordStoneID = 0,dwSwordStoneValue = 0,};
	for k,v in pairs(_G.SwordFoundryConsumeInfoConfig) do
		if(k == num) then
			consumeInfo.dwMetalValue = v.dwMetalValue;
			consumeInfo.dwWoodValue = v.dwWoodValue;
			consumeInfo.dwWaterValue = v.dwWaterValue;
			consumeInfo.dwFireValue = v.dwFireValue;
			consumeInfo.dwEarthValue = v.dwEarthValue;
			consumeInfo.dwSwordStoneID = v.dwSwordStoneID;
			consumeInfo.dwSwordStoneValue = v.dwSwordStoneValue;
			break;
		end
	end
	return consumeInfo
end

