--获取属性加成
--参数：Info = {[1] =2;[2] = 3}   --[1] =2 为当前勋章等级 [2] =3 当前勋章星级
--返回值：Attr   = {dwAttack= 0 ,dwDefense= 0,dwFlee= 0,dwCrit= 0,dwHPMax= 0,dwCritDown= 0,dwRemitAttack= 0,};  属性Table
function ErrantHonorConfig:GetAttrByLevel(Info)
	local hLevel = Info[1];
	local sLevel = Info[2];
	local Attr   = {dwHPMax= 0 ,dwDefense= 0 ,dwCritDown = 0 ,};
	if hLevel == 0 and sLevel == 0 then
		return Attr;
	end
	local Num = (hLevel - 1)*9 + sLevel
	local AttrConfig = XunZhangConfig[Num];
	for k,v in pairs(Attr) do
		Attr[k] =AttrConfig[k]
	end
	return Attr
end
--获取等级消耗
--参数：Info = {[1] =2;[2] = 3}   --[1] =2 为当前勋章等级 [2] =3 当前勋章星级
--返回值：ConNum 侠义值消耗
function ErrantHonorConfig:GetConByLevel(Info)
	local hLevel = Info[1];
	local sLevel = Info[2];
	local Num = (hLevel - 1)*9 + sLevel
	local AttrConfig = XunZhangConfig[Num];
	local ConNum = AttrConfig.dwCost
	if Num ==  #XunZhangConfig then
		ConNum = 0
	end
	return ConNum
end
--获取成功率字段
--参数：Info = {[1] =2;[2] = 3}   --[1] =2 为当前勋章等级 [2] =3 当前勋章星级
--返回值：ConNum 侠义值消耗
function ErrantHonorConfig:GetRateStringByLevel(Info)
	local hLevel = Info[1];
	local sLevel = Info[2];
	local Num = sLevel;
	local strRate = ErrantHonorConfig.strRate[Num];
	return strRate
end

