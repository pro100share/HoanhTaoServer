--随机
local function probability(data)
	local dwMax = 0;
	for XiaBiao,dwProp in pairs(data) do
		dwMax = dwMax + dwProp.Probability
	end
	local dwRes = math.random(1,dwMax)
	local dwCur = 0;
	for XiaBiao,dwProp in pairs(data) do
		dwCur = dwCur + dwProp.Probability
		if dwRes <= dwCur then
			return XiaBiao;
		end
	end
end

_G.ChestAwardConfig = {

	ChestAwardList = function(objPlayer, enumId)
		local addTable = {};
		local LeiXing = _G.ChestConfig[enumId].Fashion
		local Data = {};
		local With = _G.ChestConfig[enumId].tbReward.tbItemList
		if (LeiXing == 1) then
			Data = With;
		elseif (LeiXing == 2) then
			Data[1] = With[probability(With)];
		end
		for k,n in pairs(Data) do
			addTable[k] = {};
			addTable[k].dwItemEnum = n.dwEnumID;
			addTable[k].dwItemNumber= n.dwNumber;
			addTable[k].dwBindType = enItemBindType.eYes;
		end
		return addTable;
	end;
}

